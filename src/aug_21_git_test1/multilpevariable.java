package aug_21_git_test1;

public class multilpevariable {
	public static void main(String[] args) {
        String name;          //  variable Declaration
        int rollno;           // variable Declaration
        float marks;          // variable Declaration 
        char grade;          // variable Declaration
        
        name="mahesh";      // Variable initialization
        rollno=109;
        marks=67.33f;
        grade='A';
        
        System.out.println ("my name is"+name);      // variable usage
        System.out.println ("my roll number is"+rollno);
        System.out.println ("mymarks are"+marks);
        System.out.println ("my grade is"+grade);
                            
	}

}
